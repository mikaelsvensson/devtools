package info.mikaelsvensson.docutil.xml.extensivedocumentcreator;

public class JavadocItemHandlerException extends Exception {
// --------------------------- CONSTRUCTORS ---------------------------

    public JavadocItemHandlerException() {
    }

    public JavadocItemHandlerException(final Throwable cause) {
        super(cause);
    }

    public JavadocItemHandlerException(final String message) {
        super(message);
    }

    public JavadocItemHandlerException(final String message, final Throwable cause) {
        super(message, cause);
    }
}
